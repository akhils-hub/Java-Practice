package org.number_programs;

public class Even_Old_Number {

	public static void main(String[] args) {
		int n = 10;
		
		if(n%2 == 0) {
			System.out.println(n + " is a Even Number!");
		}else {
			System.out.println(n + " is a Odd Number!");
		}

	}

}


